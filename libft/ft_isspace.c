#include "libft.h"

int		ft_isspace(int c)
{
	c = (unsigned char)c;
	if (c == ' ' || c == '\n' || c == '\f' ||
			c == '\r' || c == '\t' || c == '\v')
		return (1);
	else
		return (0);
}
